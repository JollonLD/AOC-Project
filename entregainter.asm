fim_loop_vizinhos:
    # Desmarca nó (backtrack)
    la $t0, visitados
    sll $t1, $s0, 2
    add $t0, $t0, $t1
    sw $zero, ($t0)
    
    # Restaura e retorna
    lw $ra, 16($sp)
    lw $s0, 12($sp)
    lw $s1, 8($sp)
    lw $s2, 4($sp)
    lw $s3, 0($sp)
    addi $sp, $sp, 20
    jr $ra

encontrou_caminho:
    # tam_caminho ++
    addi $t2, $s2, 1
    la $t0, tam_caminho
    sw $t2, ($t0)
    
    # imprime distancia e tempo do caminho
    jal imprimir_caminho
    jal calcular_metricas
    
    # Desmarca para o backtrack
    la $t0, visitados
    sll $t1, $s0, 2
    add $t0, $t0, $t1
    sw $zero, ($t0)
    
    # Restaura e continua
    lw $ra, 16($sp)
    lw $s0, 12($sp)
    lw $s1, 8($sp)
    lw $s2, 4($sp)
    lw $s3, 0($sp)
    addi $sp, $sp, 20
    jr $ra

imprimir_caminho:
    # Salva PC de retorno
    addi $sp, $sp, -4
    sw $ra, 0($sp)

    la $t0, contador_caminhos
    lw $t1, ($t0)
    addi $t1, $t1, 1
    sw $t1, ($t0)
    
    # Limite de 5 caminhos
    lw $t6, max_caminhos
    bgt $t1, $t6, menu
    beqz $t1, sem_caminho

    # Imprimir número do caminho
    li $v0, 4
    la $a0, msg_caminhos
    syscall
    
    li $v0, 1
    move $a0, $t1
    syscall
    
    li $v0, 4
    la $a0, dois_pontos
    syscall
    
    # Inicializar contador do loop
    li $t0, 0
    lw $t1, tam_caminho
    
loop_imprimir:
    beq $t0, $t1, end_print
    
    # Carregar e imprimir local atual
    la $t2, caminho
    sll $t3, $t0, 2
    add $t2, $t2, $t3
    lw $t4, ($t2)
    addi $t4, $t4, 1  # Ajustar indice para 1 based
    
    jal print_lugar
    
    # Imprimir separador se não for o último elemento
    addi $t5, $t1, -1
    beq $t0, $t5, skip_separador
    li $v0, 4
    la $a0, separador2
    syscall
    
skip_separador:
    addi $t0, $t0, 1
    j loop_imprimir

end_print:
    # Imprimir nova linha
    li $v0, 4
    la $a0, enter
    syscall
    
    # Restaurar endereço de retorno e retornar
    lw $ra, 0($sp)
    addi $sp, $sp, 4
    jr $ra

calcular_metricas:
    # Salva e retorna para onde estava
    addi $sp, $sp, -8        
    sw $ra, 4($sp)
    sw $s4, 0($sp)          

    # Chamar função que calcula distância e tempo
    jal distancia_e_tempo

    # Imprimir a distância
    li $v0, 4
    la $a0, saida_distancia
    syscall

    li $v0, 2
    mov.s $f12, $f2        # Distância em km
    syscall

    # Imprimir o tempo
    li $v0, 4
    la $a0, saida_tempo
    syscall

    li $v0, 1
    move $a0, $t4         # Tempo em minutos
    syscall

    # Imprimir newline
    li $v0, 4
    la $a0, enter
    syscall

    # Restaurar registradores e retornar
    lw $s4, 0($sp)
    lw $ra, 4($sp)
    addi $sp, $sp, 8
    jr $ra

distancia_e_tempo:
    # Salvar registradores
    addi $sp, $sp, -28
    sw $ra, 24($sp)
    sw $s0, 20($sp)
    sw $s4, 16($sp)
    sw $s5, 12($sp)
    sw $s6, 8($sp)
    sw $s7, 4($sp)
    sw $t2, 0($sp)

    li $t0, 0            # Inicializar contador
    lw $t1, tam_caminho
    addi $t1, $t1, -1    # Ajustar para número de arestas
    la $t2, caminho      # Carregar endereço do caminho

    li.s $f2, 0.0        # Acumulador de distância (ponto flutuante)
    la $t5, distancias   # Carregar endereço base da matriz de distâncias

loop_calculo:
    beq $t0, $t1, fim_loop_calculo  # Alterado para novo label

    # Carregar vértices da aresta atual
    sll $t7, $t0, 2
    add $s0, $t2, $t7
    lw $s4, ($s0)        # Vértice atual
    lw $s5, 4($s0)       # Próximo vértice

    # Calcular offset na matriz de distâncias
    mul $s6, $s4, 56     # Cada linha tem 14 vértices
    add $s6, $s6, $s5    
    sll $s6, $s6, 2      
    add $s6, $t5, $s6    

    # Carregar e acumular distância
    l.s $f4, ($s6)       
    add.s $f2, $f2, $f4  

    addi $t0, $t0, 1
    j loop_calculo

fim_loop_calculo:        
    # Carregar tipo de transporte salvo
    lw $a0, 16($sp)      
    
    # Obter velocidade baseado no tipo de transporte
    jal get_velocidade
    
    # Calcular tempo
    l.s $f6, ($v0)       # Carregar velocidade
    div.s $f8, $f2, $f6  # tempo = distância / velocidade (horas)
    
    # Converter para minutos
    li.s $f6, 60.0
    mul.s $f8, $f8, $f6  
    
    # Arredondar para inteiro
    cvt.w.s $f8, $f8
    mfc1 $t4, $f8

    # Garantir mínimo de 1 minuto
    li $t5, 1
    beqz $t4, force_min_time
    j fim_tempo

force_min_time:
    move $t4, $t5

fim_tempo:
    # Restaurar registradores
    lw $ra, 24($sp)
    lw $s0, 20($sp)
    lw $s4, 16($sp)
    lw $s5, 12($sp)
    lw $s6, 8($sp)
    lw $s7, 4($sp)
    lw $t2, 0($sp)
    addi $sp, $sp, 28
    jr $ra

